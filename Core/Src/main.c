void led_task(void const *pvParameters) {

	while(1){
		printf("[   led_task] Start\n\r");
		HAL_GPIO_TogglePin(Green_LED_GPIO_Port, Green_LED_Pin);
		HAL_GPIO_TogglePin(Blue_LED_GPIO_Port, Blue_LED_Pin);
		HAL_GPIO_TogglePin(Orange_LED_GPIO_Port, Orange_LED_Pin);
		HAL_GPIO_TogglePin(Red_LED_GPIO_Port, Red_LED_Pin);
		vTaskDelay(1000);
	}
}

void switch_task(void const *pvParameters) {

	int button_state = not_pressed;

	while(1){
		if (HAL_GPIO_ReadPin(Blue_Button_GPIO_Port, Blue_Button_Pin)){
			vTaskDelay(100);
			// Debounce Checking
			if (HAL_GPIO_ReadPin(Blue_Button_GPIO_Port, Blue_Button_Pin)){
				if (button_state == not_pressed){
					button_state = pressed;
					printf("[switch_task] Send\n\r");
				}
				// else do nothing on long press
			}
			else {
				button_state = not_pressed;
			}
		}
		else {
			button_state = not_pressed;
		}
	}
}



